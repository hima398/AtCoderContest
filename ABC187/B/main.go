func main() {
	buf := make([]byte, 1024*1024)
	sc.Buffer(buf, bufio.MaxScanTokenSize)
	sc.Split(bufio.ScanWords)

	n := nextInt()
	x := make([]int, n+1)
	y := make([]int, n+1)
	for i := 1; i <= n; i++ {
		x[i], y[i] = nextInt(), nextInt()
		//fmt.Println(x[i], y[i])
	}
	ans := 0
	for i := 1; i < n; i++ {
		for j := i + 1; j <= n; j++ {
			a := float64(y[j]-y[i]) / float64(x[j]-x[i])
			//fmt.Println(a)
			if a <= 1.0 && a >= -1.0 {
				ans++
			}
		}
	}
	fmt.Println(ans)
}
